# Example 1:
grades = [90, 88, 62, 76, 74, 89, 48, 57]
doubled = []
scaled_grades = [num + 10 for num in grades]
print(scaled_grades)

for grade in grades:
  doubled.append(grade * 2)
print(doubled)


